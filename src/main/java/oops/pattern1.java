package oops;

public class pattern1 {

    public static void main(String[] args) {
        int n=5;
        int m=5;

       for(int i=1; i<=n; i++) {
    	   
    	   
    	   for(int j=0; j<=m; j++) {
    		   
    		   System.out.print("");
    		  if(i==1 || j==1 || n==i || m==j) {
    			  
    			  System.out.print("*");
    		  }
    	   }
    	   
    	   System.out.println(" ");
       }
    }
} 
          
        
   

